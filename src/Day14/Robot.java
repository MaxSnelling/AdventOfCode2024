package Day14;

public class Robot {
    public int xPos;
    public int yPos;
    public int xVelocity;
    public int yVelocity;

    public Robot(int xPos, int yPos, int xVelocity, int yVelocity) {
        this.xPos = xPos;
        this.yPos = yPos;
        this.xVelocity = xVelocity;
        this.yVelocity = yVelocity;
    }
}
